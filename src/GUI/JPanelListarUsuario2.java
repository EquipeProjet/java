    private Object[][] carregarDados(ArrayList<Usuario> lista) {
        Object[][] dados = new Object[lista.size()][4];
        
        

        for (int i = 0; i < lista.size(); i++) {
            dados[i][0] = lista.get(i).getId();
            dados[i][1] = lista.get(i).getLogin();
            dados[i][2] = lista.get(i).getSenha();
            //dados[i][3] = "Editar";
            //dados[i][3] = lista.get(i).getLogin();
        }
        return dados;
    }

