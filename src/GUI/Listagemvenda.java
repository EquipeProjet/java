         private Object[][] carregarDados(ArrayList<Venda> lista) {
                 Object[][] dados = new Object[lista.size()][6];
         
        for (int i = 0; i < lista.size(); i++) {
            dados[i][0] = lista.get(i).getId();
            dados[i][1] = lista.get(i).getData();
           dados[i][2] = lista.get(i).getNomeCliente();
            dados[i][3] = lista.get(i).getNomeProduto();
            dados[i][4] = lista.get(i).getValorVenda();
           dados[i][5] = lista.get(i).getQuant();
           // dados[i][6] = lista.get(i).getCep();
           // dados[i][7] = lista.get(i).getBairro();
           
        }
         return dados;
    
         }

